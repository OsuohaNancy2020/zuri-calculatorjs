var operator = prompt("operator: - / * +");
var num1 = parseInt (prompt('num1: '))
var num2 = parseInt (prompt('num2: '))

if (operator === '+') {alert(num1+num2)}
   else if (operator === '-') {alert(num1-num2)}
   else if (operator === '*') {alert(num1*num2)}
   else if (operator === '/') {alert(num1/num2)}
    